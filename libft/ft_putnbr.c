#include "libft.h"

void	ft_putnbr(long int n)
{
	if (n < 0)
	{
		ft_putchar(45);
		n = -n;
	}
	if (n >= 0 && n <= 9)
	{
		ft_putchar(n + 48);
	}
	if (n >= 10)
	{
		ft_putnbr(n / 10);
		ft_putnbr(n % 10);
	}
}
